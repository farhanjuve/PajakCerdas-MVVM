package com.example.ryzen.pajakcerdas;

public class InformasiObjek {
    private String alamat;
    private String harga;
    private String njkp;
    private String pbb;

    public InformasiObjek(){

    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getNjkp() {
        return njkp;
    }

    public void setNjkp(String njkp) {
        this.njkp = njkp;
    }

    public String getPbb() {
        return pbb;
    }

    public void setPbb(String pbb) {
        this.pbb = pbb;
    }
}
